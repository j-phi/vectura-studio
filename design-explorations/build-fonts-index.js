/* Builds a static snapshot of the full Google Fonts catalog for the Text-panel
   font picker. Fetches the keyless metadata endpoint server-side (CORS only
   restricts browsers, not Node), maps each family to [name, shortCategory],
   sorts by popularity, and emits text-panel-fonts.js (window.VECTURA_GFONTS).

   Re-run to refresh the snapshot:  node design-explorations/build-fonts-index.js
   Requires Node 18+ (global fetch). */
const fs = require('fs');
const path = require('path');

const ENDPOINT = 'https://fonts.google.com/metadata/fonts';
const OUT = path.join(__dirname, 'text-panel-fonts.js');

// Google category string -> our short code used by the picker (gFallback()).
const CAT = {
  'Sans Serif': 's',
  'Serif': 'f',
  'Display': 'd',
  'Monospace': 'm',
  'Handwriting': 'h'
};

async function main() {
  process.stdout.write('Fetching ' + ENDPOINT + ' …\n');
  const res = await fetch(ENDPOINT, { headers: { 'Accept': 'application/json' } });
  if (!res.ok) throw new Error('HTTP ' + res.status + ' ' + res.statusText);

  // Strip the XSSI guard prefix ( )]}' ) Google prepends to this JSON.
  let body = await res.text();
  body = body.replace(/^[^{[]+/, '');
  const data = JSON.parse(body);

  const list = data.familyMetadataList || data.items || [];
  if (!list.length) throw new Error('No families found in response');

  // Sort by popularity rank (1 = most popular); families without a rank go last.
  const sorted = list.slice().sort((a, b) => {
    const pa = typeof a.popularity === 'number' ? a.popularity : 1e9;
    const pb = typeof b.popularity === 'number' ? b.popularity : 1e9;
    return pa - pb;
  });

  // Extra filter tags beyond the primary category, packed into a compact string:
  //   b=slab/block  v=variable  w=variable-width (wdth axis)  i=has italics  c=color font
  const extrasOf = (f) => {
    const axes = (f.axes || []).map(a => a.tag);
    const fontKeys = Object.keys(f.fonts || {});
    let ex = '';
    if (axes.length) ex += 'v';
    if (axes.indexOf('wdth') >= 0) ex += 'w';
    if (fontKeys.some(k => /i$/.test(k))) ex += 'i';
    if (f.stroke === 'Slab Serif') ex += 'b';
    if (Array.isArray(f.colorCapabilities) && f.colorCapabilities.length) ex += 'c';
    return ex;
  };

  const tuples = [];
  const seen = new Set();
  let unknownCat = 0;
  for (const f of sorted) {
    const name = f.family || f.displayName;
    if (!name || seen.has(name)) continue;
    seen.add(name);
    const cat = CAT[f.category];
    if (!cat) unknownCat++;
    const ex = extrasOf(f);
    // Omit the 3rd element when there are no extras, to keep the file small.
    tuples.push(ex ? [name, cat || 's', ex] : [name, cat || 's']);
  }

  // Emit ~10 tuples per line so the generated file stays readable / diff-able.
  const rows = [];
  for (let i = 0; i < tuples.length; i += 10) {
    rows.push('  ' + tuples.slice(i, i + 10).map(t => JSON.stringify(t)).join(',') + ',');
  }
  const out =
    '/* GENERATED by build-fonts-index.js — do not hand-edit.\n' +
    '   Snapshot of the full Google Fonts catalog, popularity-ordered.\n' +
    '   Tuple: [name, category, extras?]\n' +
    '     category: s=sans f=serif d=display m=mono h=handwriting\n' +
    '     extras:   b=slab/block v=variable w=variable-width i=italics c=color */\n' +
    'window.VECTURA_GFONTS = [\n' +
    rows.join('\n') + '\n];\n';

  fs.writeFileSync(OUT, out, 'utf8');
  process.stdout.write('Wrote ' + tuples.length + ' families -> ' + OUT + '\n');
  if (unknownCat) process.stdout.write('  (' + unknownCat + ' had an unmapped category, defaulted to sans)\n');
}

main().catch(err => { console.error('FAILED:', err.message); process.exit(1); });
