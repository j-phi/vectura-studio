#!/usr/bin/env node
/**
 * Regenerates src/config/user-wallpaper-recipes.js from .vectura files placed in
 * user-presets/wallpaper/.
 *
 * Usage: npm run user-presets:bundle (runs alongside build-user-presets.js)
 *
 * A wallpaper recipe is NOT an algorithm-params preset — it is a mirror-modifier
 * config. For each .vectura file the script finds the modifier layer, locates its
 * wallpaper mirror, and emits a recipe in the gallery's public shape:
 *   { id, name, mirror }
 * matching WallpaperPresets.list() so the recipe flows straight into the gallery
 * (appended in wallpaper-presets.js list()). Runtime-only mirror fields (id,
 * color) are stripped so each application gets a fresh id and palette color.
 *
 * The recipe name defaults to the title-cased filename stem; set a top-level
 * "name" key in the .vectura JSON to override it.
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const OUTPUT_PATH = path.join(REPO_ROOT, 'src/config/user-wallpaper-recipes.js');
const WALLPAPER_DIR = path.join(REPO_ROOT, 'user-presets', 'wallpaper');

// Mirror fields that are runtime/instance-specific and must not be frozen into a
// reusable recipe (a fresh id + palette color are assigned when applied).
const STRIP_MIRROR_KEYS = new Set(['id', 'color']);

const toTitleCase = (str) =>
  str.replace(/[-_]+/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase());

const slugify = (str) =>
  str.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

const isModifierLayer = (layer) =>
  Boolean(layer && layer.isGroup && layer.containerRole === 'modifier' && layer.modifier);

const findWallpaperMirror = (doc) => {
  const layers = Array.isArray(doc.layers) ? doc.layers : [];
  for (const layer of layers) {
    if (!isModifierLayer(layer)) continue;
    // Mirrors live at layer.modifier.mirrors; tolerate a flattened layer.mirrors.
    const mirrors = layer.modifier?.mirrors || layer.mirrors || [];
    const wp = mirrors.find((m) => m && m.type === 'wallpaper');
    if (wp) return wp;
  }
  return null;
};

const sanitizeMirror = (mirror) => {
  const out = {};
  for (const [k, v] of Object.entries(mirror)) {
    if (!STRIP_MIRROR_KEYS.has(k)) out[k] = v;
  }
  return out;
};

const recipes = [];

if (fs.existsSync(WALLPAPER_DIR)) {
  const files = fs.readdirSync(WALLPAPER_DIR)
    .filter((f) => f.toLowerCase().endsWith('.vectura'))
    .sort((a, b) => a.localeCompare(b));

  for (const file of files) {
    const filePath = path.join(WALLPAPER_DIR, file);
    let doc;
    try {
      doc = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    } catch (e) {
      console.warn(`[user-wallpaper:bundle] Skipping ${file}: JSON parse error — ${e.message}`);
      continue;
    }

    const mirror = findWallpaperMirror(doc);
    if (!mirror) {
      console.warn(`[user-wallpaper:bundle] Skipping ${file}: no wallpaper mirror found`);
      continue;
    }

    const stem = path.basename(file, '.vectura');
    const name = (typeof doc.name === 'string' && doc.name.trim()) || toTitleCase(stem);
    const id = `wallpaper-user-${slugify(stem)}`;
    recipes.push({ id, name, mirror: sanitizeMirror(mirror) });
    console.log(`[user-wallpaper:bundle] Added: ${id} ("${name}")`);
  }
}

const recipesJson = JSON.stringify(recipes, null, 2)
  .split('\n')
  .map((line) => '    ' + line)
  .join('\n')
  .trim();

const output = `(() => {
  'use strict';
  // Auto-generated by scripts/build-user-wallpaper-recipes.js — do not edit manually.
  // To add wallpaper recipes: drop .vectura files (with a wallpaper mirror) into
  // user-presets/wallpaper/, then run: npm run user-presets:bundle
  const Vectura = (window.Vectura = window.Vectura || {});
  Vectura.USER_WALLPAPER_RECIPES = ${recipesJson};
})();
`;

fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
console.log(`[user-wallpaper:bundle] Wrote ${recipes.length} recipe(s) to src/config/user-wallpaper-recipes.js`);
